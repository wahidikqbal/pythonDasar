
def bagi(a,b):
    print('fungsi bagi')
    c = a / b
    print(a,':',b,'=',c)

def kali(a,b):
    print('fungsi kali')
    c = a * b
    print(a,'x',b,'=',c)

def kurang(a,b):
    print('fungsi kurang')
    c = a - b
    print(a,'-',b,'=',c)

def tambah(a,b):
    print('fungsi tambah')
    c = a + b
    print(a,'+',b,'=',c)

